// Math operations

//result will be integer
console.log(10 + 4);
console.log(10 - 4);
console.log(10 * 4);
//result will be a float - 2.5 has decimal
console.log(10 / 4);
//modular calculation which means we take the remaining number of the division
console.log(10 % 4);// 10 / 4 => 2.5 => 2 * 4 = 8 => remaining 2
console.log(10 % 3);// 3 into 10 is 3 times and remaining 1

console.log((10 + 3 - 5) * 10);
let result = (10 + 3 - 5) * 10;
result = 10 * 4;
result++;// result = result + 1
result--;// result = result - 1
result+= 5;// result = result + 5
result/= 5;// result = result / 5
result*= 5;// result = result * 5

console.log(result);

